                    bookData.authorName ?? 'No Name',
                    style: Styles.textStyle14,
                  ),
                  const SizedBox(
                    height: 3,
                  ),
                  Row(
                    children: [
                      Text(
                        bookData.price == 0.0 ? 'Free' : '${bookData.price} â‚¬',
                        style: Styles.textStyle20.copyWith(
                          fontWeight: FontWeight.bold,
